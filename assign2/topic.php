                <h2 class="period">2.20th Century</h2>
                <section>
                    <p>
                        In 1931, Vannevar Bush and his teammates at MIT invented the Differential Analyzer, which is a device to calculate and solve problems relating to calculus, derivatives, practically. It is accounted the first analog computer. However, it is still mechanical. Five years later, Alan Turing, a scientist and mathematician has invented and published a new computing model from which computers nowadays used the concept. It is called Turing machine – an unlimited tape with a head to execute and record some of the basic actions at that moment, e.g., writing, reading, acknowledging the state, Turing was then become well-known and he has been invited to a project that can help solving the Nazi code in World War II.
                    </p>
