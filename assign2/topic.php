            <h2 class="period"> 1.Early 19th Century</h2>
            <section>
                    <p>
                        The history of modern computing rooted back to the 1801s, when the first automatically weave fabric designs was invented. The French man inventor, Joseph Marie Jacquard, created an automatic fabric weaving loom using punched wooden cards. The mechanism of the invention can also be seen in today’s computer. Twenty-years late, A machine that could compute tables of numbers was invented by Charles Babbage and his son Henry.  It eventually named the Babbage’s Analytical Engine. Programmed using punch cards, the machine featured ALU, integrated memory and basic flow control.
                    </p>
